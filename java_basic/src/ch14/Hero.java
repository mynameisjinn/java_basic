package ch14;

public class Hero {

	private String name;
	private int level;
	
	// getter
	public String getName() {
		return this.name;
	}
	public int getLevel() {
		return this.level;
	}
	
	// setter
	public void setName(String name) {
		this.name = name;
	}
	public void setLevel(int level) {
		this.level = level;
	}
}
