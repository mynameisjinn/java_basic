package ch01;

public class DataTypeEx1 {
	
// 코드의 시작점(메인함수)
	public static void main(String[] args) {
		
		// 크게 데이터 타입 종류는 2가지가 있음
		// 1. 기본 데이터 타입 - ( 정수, 실수, 문자, boolean )
		// 2. 참조 타입 - ( 주소값 ) 
		
		// 데이터 타입의 종류
		
		// 정수형 데이터 타입 (1,10,100 등의 정수형의 데이터를 담을 수 있음) 
		byte b;   // 1  byte(-128~127) 
		b = 127;
		// b = 128 -> error  발생 (지정한 정수형의 범위를 넘어 데이터를 담을 수 없음) 
		short s;  // 2 byte
		int i;   // 4 byte +- 약 22억
		
		long l;  // 8 byte  : 접미사
		l = 200000000L;
		
		
		// 실수형 데이터 타입
		
	} // end of main

} // end of class
